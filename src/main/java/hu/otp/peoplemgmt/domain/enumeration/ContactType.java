package hu.otp.peoplemgmt.domain.enumeration;

/**
 * Enumeration representing the types of a Contact.
 * @author Andras Nyilas
 */
public enum ContactType {

    /**
     * Indicates a telephone Contact.
     */
    TELEPHONE,

    /**
     * Indicates an email Contact.
     */
    EMAIL,

    /**
     * Indicates a fax Contact.
     */
    FAX,

    /**
     * Indicates a URL Contact.
     */
    URL

}
